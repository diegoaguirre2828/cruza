#!/usr/bin/env node
// One-shot push-pipe end-to-end verifier. Reads every active push_subscription
// row from prod Supabase, attempts a webpush via the configured VAPID keys,
// classifies the result. Run from repo root:
//   node scripts/test-push-pipe.mjs
//
// Pass means: at least one subscription receives 2xx, OR all failures are
// 410 Gone (expired subs — that's the back half working as designed,
// rejecting dead subscriptions; cron alert sends would just skip them).
// Fail means: any 4xx that isn't 410 — VAPID mismatch, or 5xx server bug.

import { createRequire } from 'node:module'
const require = createRequire(import.meta.url)
const dotenv = require('dotenv')
dotenv.config({ path: '.env.local' })

import webpush from 'web-push'
import { createClient } from '@supabase/supabase-js'

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL
const SUPABASE_SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY
const VAPID_PUBLIC = process.env.NEXT_PUBLIC_VAPID_PUBLIC_KEY || process.env.VAPID_PUBLIC_KEY
const VAPID_PRIVATE = process.env.VAPID_PRIVATE_KEY

if (!SUPABASE_URL || !SUPABASE_SERVICE_KEY) {
  console.error('Missing SUPABASE creds in .env.local')
  process.exit(1)
}
if (!VAPID_PUBLIC || !VAPID_PRIVATE) {
  console.error('Missing VAPID keys in .env.local')
  process.exit(1)
}

webpush.setVapidDetails('mailto:diegonaguirre@icloud.com', VAPID_PUBLIC, VAPID_PRIVATE)

const sb = createClient(SUPABASE_URL, SUPABASE_SERVICE_KEY)

const { data: subs, error } = await sb
  .from('push_subscriptions')
  .select('id, user_id, endpoint, p256dh, auth, created_at')
  .order('created_at', { ascending: false })

if (error) {
  console.error('SQL error:', error)
  process.exit(1)
}

console.log(`Found ${subs.length} subscription(s).\n`)

const payload = JSON.stringify({
  title: 'Cruzar — push pipe test',
  body: `End-to-end verifier @ ${new Date().toISOString()}`,
  tag: 'pipe-test',
  url: '/',
})

let delivered = 0
let gone410 = 0
let otherFail = 0
const failures = []

for (const s of subs) {
  const provider = s.endpoint.includes('fcm.googleapis.com') ? 'FCM' : s.endpoint.includes('apple.com') ? 'Apple' : 'other'
  try {
    const res = await webpush.sendNotification(
      { endpoint: s.endpoint, keys: { p256dh: s.p256dh, auth: s.auth } },
      payload
    )
    delivered++
    console.log(`✅ [${provider}] sub ${s.id} → ${res.statusCode}`)
  } catch (e) {
    if (e.statusCode === 410 || e.statusCode === 404) {
      gone410++
      console.log(`💀 [${provider}] sub ${s.id} → ${e.statusCode} Gone (expected for old subs)`)
    } else {
      otherFail++
      failures.push({ provider, id: s.id, status: e.statusCode, body: e.body })
      console.log(`❌ [${provider}] sub ${s.id} → ${e.statusCode}: ${e.body || e.message}`)
    }
  }
}

console.log('\n— Summary —')
console.log(`Delivered (2xx):       ${delivered}`)
console.log(`Gone (410/404):        ${gone410}`)
console.log(`Other failures:        ${otherFail}`)

if (otherFail > 0) {
  console.log('\nFailure details:')
  console.log(JSON.stringify(failures, null, 2))
  console.log('\nPipe BROKEN — non-410 failures indicate VAPID mismatch or server issue.')
  process.exit(2)
}

if (delivered === 0 && gone410 === subs.length) {
  console.log('\nAll subscriptions are dead (410 Gone). Pipe code is correct but no live subs to confirm delivery.')
  console.log('Subscriptions need to be regenerated by users opening the app + tapping bell.')
  process.exit(0)
}

if (delivered > 0) {
  console.log('\n✅ Pipe VERIFIED — at least one subscription received 2xx.')
  process.exit(0)
}
